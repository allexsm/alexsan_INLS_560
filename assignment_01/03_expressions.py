# Variables
a = 4
b = 2
c = 3

# Default order of operations
result1 = a + b * c
print("a + b * c =", result1)

# With parentheses addition first
result2 = (a + b) * c
print("(a + b) * c =", result2)

#More variations
result3 = a * (b + c)
print("a * (b + c) =", result3)

result4 = (a + b + c)
print("(a + b + c) =", result4)